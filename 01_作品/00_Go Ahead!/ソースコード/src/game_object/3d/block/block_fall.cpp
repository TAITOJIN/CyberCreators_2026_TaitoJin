void BlockFall::inspector()
{
	// 親クラスの処理
	BlockBase::inspector();
}

//---------------------------------------------------
// 保存
//---------------------------------------------------
void BlockFall::save(json& outObject) const
{
	// 親クラスの処理
	BlockBase::save(outObject);
}

//---------------------------------------------------
// ムーブ代入演算子
//---------------------------------------------------
BlockFall& BlockFall::operator=(BlockFall&& right) noexcept
{
	if (this != &right)
	{
		// 親クラスの処理
		BlockBase::operator=(std::move(right));
	}

	return *this;
}
